package otp.model;

/**
 * Перечисление статусов OTP-кодов.
 * 
 * Основные статусы:
 * 1. ACTIVE (Активный)
 *    - Состояние
 *      * Только что создан
 *      * В пределах TTL
 *      * Готов к использованию
 *      * Валидный для проверки
 *      * Доступен для операций
 *    - Ограничения
 *      * Одноразовое использование
 *      * Временное действие
 *      * Привязка к операции
 *      * Контроль времени жизни
 *      * Проверка валидности
 * 
 * 2. USED (Использованный)
 *    - Причины
 *      * Успешная валидация
 *      * Подтверждение операции
 *      * Завершение цикла
 *      * Выполнение действия
 *      * Завершение транзакции
 *    - Последствия
 *      * Блокировка повторного использования
 *      * Сохранение в истории
 *      * Отметка в логах
 *      * Аудит операции
 *      * Обновление статистики
 * 
 * 3. EXPIRED (Просроченный)
 *    - Причины
 *      * Истечение TTL
 *      * Превышение лимита времени
 *      * Автоматическая инвалидация
 *      * Отмена операции
 *      * Системная блокировка
 *    - Действия
 *      * Требуется новый код
 *      * Очистка из кэша
 *      * Архивация в БД
 *      * Уведомление пользователя
 *      * Обновление статуса
 * 
 * Безопасность:
 * - Контроль времени жизни
 * - Защита от повторного использования
 * - Автоматическая инвалидация
 * - Мониторинг состояния
 * - Аудит операций
 * - Валидация статусов
 * - Контроль изменений
 * - Защита от манипуляций
 * - Логирование действий
 * - Реагирование на инциденты
 */
public enum OtpStatus {
    /** 
     * Активный код, готовый к использованию.
     * 
     * Характеристики:
     * - Валидный для проверки
     * - В пределах TTL
     * - Готов к использованию
     * - Привязан к операции
     * - Доступен для валидации
     * 
     * Ограничения:
     * - Одноразовое использование
     * - Временное действие
     * - Контроль времени жизни
     * - Привязка к операции
     * - Проверка валидности
     */
    ACTIVE,

    /** 
     * Код, который был успешно использован.
     * 
     * Характеристики:
     * - Завершенное использование
     * - Подтвержденная операция
     * - Завершенный цикл
     * - Выполненное действие
     * - Завершенная транзакция
     * 
     * Ограничения:
     * - Нельзя использовать повторно
     * - Только для истории
     * - Требуется новый код
     * - Блокировка операций
     * - Архивация данных
     */
    USED,

    /** 
     * Код, у которого истек срок действия.
     * 
     * Характеристики:
     * - Истекший TTL
     * - Превышен лимит времени
     * - Автоматическая инвалидация
     * - Отмененная операция
     * - Системная блокировка
     * 
     * Ограничения:
     * - Требуется новый код
     * - Нельзя использовать
     * - Только для истории
     * - Архивация в БД
     * - Очистка из кэша
     */
    EXPIRED
}

