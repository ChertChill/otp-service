package otp.service.notification;

/**
 * Каналы отправки OTP-кодов пользователям.
 * 
 * Поддерживаемые каналы:
 * - EMAIL: отправка кода на электронную почту
 * - SMS: отправка кода через SMS-сообщение
 * - TELEGRAM: отправка кода через Telegram бота
 * - FILE: запись кода в файл (для тестирования)
 * 
 * Примечание:
 * - Каждый канал реализует свой способ доставки
 * - Выбор канала зависит от настроек пользователя
 * - Все каналы обеспечивают безопасную доставку кода
 */
public enum NotificationChannel {
    EMAIL,
    SMS,
    TELEGRAM,
    FILE
}

